class UnaryExample{
public static void main(String args[])
{//using unary plus operator
int number=+76;//print the positive value of the variable number
System.out.println(number);
//using unary minus operator
number=-number;//print the negative of the variable number
System.out.println(number);
int a=20;
a++;//increments the value of variable by 1
System.out.println(a);
a--;//decrements value of variable
System.out.println(a);
boolean flag=false;//declaring a boolean variable
//print the value of success variable
System.out.println(flag);//prints complement of variable
System.out.println(!flag);
}
}

